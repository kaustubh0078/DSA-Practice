package Lecture1;

class ArithmaticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+b);  //Addition Operator
        System.out.println(b-a);  //Subtraction Operator
        System.out.println(a*b);  //Multiplication Operator
        System.out.println(b/a);  //Division Operator
        System.out.println(a%b);  //Modulo Operator

    }
}
